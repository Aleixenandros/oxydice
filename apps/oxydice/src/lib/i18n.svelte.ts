const messages: Record<Lang, Dict> = { es, en, de, pt };

let lang = $state<Lang>("es");

/** Idioma soportado a partir de `navigator.language` (cae a `es`). */
export function detectLang(): Lang {
  const p = (
    typeof navigator !== "undefined" ? navigator.language : "es"
  )
    .slice(0, 2)
    .toLowerCase();
  return (["es", "en", "de", "pt"] as const).includes(p as Lang)
    ? (p as Lang)
    : "es";
}

export function getLang(): Lang {
  return lang;
}

export function setLang(l: Lang) {
  lang = l;
}

/** Traduce `key` con interpolación posicional `{0}`, `{1}`, … */
export function t(key: string, ...args: (string | number)[]): string {
  let s = messages[lang][key] ?? es[key] ?? key;
  args.forEach((a, i) => {
    s = s.replaceAll(`{${i}}`, String(a));
  });
  return s;
}
